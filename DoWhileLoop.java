package Loops;

public class DoWhileLoop {

    // Unlike while and for loop,in do-while loop i.e. the loop is executed at least once before the condition is checked other than that everything is the same as in the while loop.

    public static void main(String[] args) {
        int i =0;
        do {
            System.out.println("*");
            i++;
        } while (i<5);
    }
}
